# Perform Basic Mathematical Operations


a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))

# Performing operations
add = a + b
sub = a - b
mul = a * b
div = a / b

# Displaying results
print("\nAddition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)
